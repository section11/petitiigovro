				PETIȚII.GOV.RO pune la dispoziția cetățenilor o modalitate interactivă pentru a petiționa Guvernul României pe diferite domenii de interes. Acest instrument a fost creat întrucât ne dorim să auzim direct de la voi care sunt problemele imediate asupra cărora trebuie să acționăm.<br><br>
				Atunci când o petiție întrunește numărul necesar de susținători, aceasta ajunge în atenția personalului specializat la nivelul Palatului Victoria în domeniul respectiv. În pasul următor ne angajăm să căutăm soluția optimă sau, după caz, să punem laolaltă factorii de decizie abilitați pentru a emite un punct de vedere oficial, care va fi redat în secțiunea <a href="<?php echo base_url();?>raspunsuri">Răspunsuri</a>.<br><br>
				Ideea de a petiționa administrația centrală prin intermediul mediului online nu ne aparține. Atât <a href="http://petitions.whitehouse.gov">Casa Albă</a>, cât și <a href="http://epetitions.direct.gov.uk/">Guvernul Regatului Unit</a> operează astfel de platforme deja de ceva timp și ne bucură faptul că există exemple de bună practică pe care le putem urma și îmbunătăți.<br><br>
				Având în vedere că această inițiativă este una inovatoare pentru felul în care Guvernul României a acționat până în prezent, ne păstrăm rezerva ca – pe măsură ce lucrurile vor evolua – să aducem ajustări menite să îmbunătățească platforma și experiența utilizatorului în ansamblul său. De asemenea, există posibilitatea să fim nevoiți să modificăm pragul necesar de semnături în vederea formulării răspunsurilor într-un timp cât mai util posibil. Pentru a vedea nivelul actual al pragului necesar accesează secțiunea <a href="#ghid-cond-participare">Condiții de participare</a>.<br><br>
				Toată ideea din spatele acestui site este de a facilita dialogul dintre guvernanţi și cei guvernaţi. Dacă ai sugestii pentru îmbunătăţirea acestui instrument, scrie-ne folosind informațiile din secțiunea <a href="<?php echo base_url();?>contact">Contact</a>.
