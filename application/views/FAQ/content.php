				<i><b>1. Creezi un cont</i></b><br><br>
				Atât pentru a semna o petiție existentă, precum și pentru a depune o petiție nouă, este necesară înregistrarea unui nume de utilizator. Toți vizitatorii cu vârsta de cel puțin 14 ani sunt eligibili în acest sens. Înregistrarea necesită introducerea unei adrese de e-mail valabile. Poți crea un cont nou de utilizator accesând opțiunea <a href="<?php echo base_url();?>inregistrare">Înregistrare</a>.<br><br>
				<i><b>2. Te conectezi</i></b><br><br>
				Odată validat contul, te poți conecta accesând opțiunea <a href="<?php echo base_url();?>ghid#login-modal">Autentificare</a>.<br><br>
				<i><b>3. Creezi o petiție</i></b><br><br>
				Înainte de a crea o petiție nouă este esențial să verifici dacă problema sau recomandarea ta nu constituie deja subiectul unei alte petiții transmise anterior. Pentru a efectua această verificare, accesează secțiunea <a href="<?php echo base_url();?>petitii_deschise">Petiții deschise</a> și folosește opțiunea de căutare disponibilă acolo.<br><br>
				Dacă există deja o petiție identică ori relativ similară, atunci te vom ruga să o semnezi pe aceea, fără a crea o alta nouă, în așa fel încât să concentrăm expresia voinței cetățenilor într-o singură inițiativă. Semnând o petiție deja existentă vei contribui la atingerea pragului stabilit, subliniind astfel necesitatea unei reacții oficiale.<br><br>
				În situația în care nu există deja o petiție ce acoperă demersul tău, atunci acesta este momentul pentru a lua în considerare care este scopul său punctual. Pentru aceasta, trebuie să știi care este problema specifică ce necesită intervenția Guvernului României ori a Prim-ministrului.<br><br> 
				PETIȚII.GOV.RO a fost gândit pentru a ridica probleme în interesul general al societății în care trăim. În acest sens trebuie, totodată, să analizezi dacă ceilalți utilizatori vor sprijini cauza ta. Acest exercițiu te va ajuta să îți articulezi mai bine mesajul, contribuind astfel la eficiența întregii proceduri.<br><br> 
				<b>3.1.</b> Pentru a adăuga o petiție nouă, deschide secțiunea + <a href="<?php echo base_url();?>petitie_noua">Petiție nouă</a>. Introdu apoi titlul petiției tale ca o continuare a propoziției „Propunerea mea pentru Guvernul României” este</i> …. Titlul trebuie să reprezinte o descriere succintă a obiectului petiției tale, rațiune pentru care acesta nu trebuie să depășească 100 caractere (respectiv litere, semne de punctuație și spații). Exemple de titlu pot fi: 
				<ul><li><i>Introducerea cadrului bugetar multianual pentru predictibilitate sporită</i></li>
				<li><i>Modificarea procedurii pentru obținerea permiselor de conducere</i></li>
				<li><i>Reglementarea unui sistem de recrutare a tinerilor în administrație</i></li></ul></p>
				<p align="justify">Titlurile trebuie să conțină diacritice, în acord cu normele ortografice în vigoare și nu trebuie să fie sintagme scrise în totalitate în majuscule ori care să conțină utilizarea în exces a semnelor de punctuație inutile.<br><br>
				<b>3.2.</b> Textul petiției reprezintă descrierea propriu-zisă a propunerii pe care o înaintezi, respectiv spațiul în care poți expune pe larg problematica petiției tale și soluția optimă pe care o propui. Din considerente obiective, am limitat conținutul câmpului la 1.000 caractere.<br><br>
				<b>3.3.</b> Categoriile corespund domeniului de activitate aferent petiției tale. Întrucât există posibilitatea ca o petiție să necesite atenția mai multor factori decidenți, am implementat opțiunea ca utilizatorii să poată alege până la trei categorii distincte.<br><br>
				<b>3.4.</b> Odată parcurși pașii anteriori, poți <i>previzualiza</i> petiția ta, o poți <i>salva</i> pentru completări ulterioare sau o poți <i>trimite</i> spre aprobare. Opțiunea de previzualizare este disponibilă în așa fel încât să poți vedea cum va arăta petiția ta pentru ceilalți. Poți salva petiția în situația în care ești de părere că ea necesită completări ulterioare. Dacă ești convins de conținutul acesteia, o poți transmite spre aprobare. Trebuie reținut faptul că toate petițiile trec prin filtrul de moderare al echipei ce administrează inițiativa. Și la fel de important este faptul că odată transmisă, o petiție nu poate fi modificată din considerentul faptului că există posibilitatea ca eventualii semnatari să nu mai agreeze sprijinirea acelei inițiative.<br><br>
				<i><b>4. Ceilalți o semnează</i></b><br><br>
				După ce petiția este transmisă, vei primi confirmarea prin mesageria integrată atunci când aceasta va fi aprobată. Te poți folosi de rețelele de socializare și de contactele tale pentru a promova petiția depusă. Amintește-ți că pentru a primi un răspuns oficial din partea Guvernului, este necesar ca petiția ta să întrunească 10.000 semnături într-un interval de 30 de zile de la data aprobării acesteia.<br><br>
				<i><b>5. Guvernul răspunde</i></b><br><br>
				Atunci când petiția atinge pragul necesar, ea este transmisă către echipa de administrare și sunt plasate în lista inițiativelor ce așteaptă un răspuns oficial. Odată emis acest răspuns, toți semnatarii petiției primesc o notificare prin mesageria internă cu privire la acest lucru. Răspunsul se va atașa automat petiției inițiale.
			</p>
